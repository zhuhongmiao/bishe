const sortedPhrases = Object.keys(fallbackTranslations).sort((a, b) => b.length - a.length)

const escapeRegExp = (value) => value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')

const translatePhrase = (text) => {
  let translated = text
  for (const phrase of sortedPhrases) {
    const regex = new RegExp(`(?<![A-Za-z])${escapeRegExp(phrase)}(?![A-Za-z])`, 'g')
    translated = translated.replace(regex, fallbackTranslations[phrase])
  }
  translated = translated.replace(/\s+/g, ' ').trim()
  return translated
}

export const formatBilingualValue = (value, fallback = '暂无 / N/A') => {
  if (value === null || value === undefined || value === '') return fallback
  const text = String(value).trim()
  if (!text) return fallback
  if (text.includes('(') && text.includes(')')) return text

  const translated = translatePhrase(text)
  if (translated && translated !== text) {
    return `${text}(${translated})`
  }

  const direct = fallbackTranslations[text]
  if (direct) return `${text}(${direct})`
  return text
}

export const formatChineseValue = (value, fallback = '暂无') => {
  if (value === null || value === undefined || value === '') return fallback
  const text = String(value).trim()
  if (!text) return fallback

  const translated = translatePhrase(text)
  if (translated && translated !== text) {
    return translated
  }

  return fallbackTranslations[text] || text
}

export const mapBilingualOptions = (values = [], bilingual = true) =>
  values.map((item) => ({
    label: bilingual ? formatBilingualValue(item) : formatChineseValue(item),
    value: item,
  }))
